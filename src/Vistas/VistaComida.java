    private void cargarCombo() {
        jCBTipo.addItem(null);
        jCBTipo.addItem("Desayuno");
        jCBTipo.addItem("Snack");
        jCBTipo.addItem("Almuerzo");
        jCBTipo.addItem("Merienda");
        jCBTipo.addItem("Cena");
    }

    private void limpiarCampos() {
        jLID.setText("ID: --");
        jTFNombre.setText("");
        jTFCalorias_por_100g.setText("");
        jCBTipo.setSelectedItem(0);
        jTADetalle.setText("");
    }

    private void activarCampos() {
        jTFNombre.setEnabled(true);
        jTFCalorias_por_100g.setEnabled(true);
        jCBTipo.setEnabled(true);
        jTADetalle.setEnabled(true);
    }
