    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        String entrada = JOptionPane.showInputDialog(this, "Ingrese el nombre completo de la comida");
        comidaActual = cd.buscarComidaPorNombre(entrada);
        if (comidaActual != null) {
            jLID.setText("ID: " + comidaActual.getId_comida());
            jTFNombre.setText(comidaActual.getNombre());
            jTFCalorias_por_100g.setText(String.valueOf(comidaActual.getCalorias_por_100g()));
            jTADetalle.setText(comidaActual.getDetalle());
            String tipoComida = comidaActual.getTipo();
            for (int i = 0; i < jCBTipo.getItemCount(); i++) {
                Object item = jCBTipo.getItemAt(i);
                if (item != null && item.toString().equalsIgnoreCase(tipoComida)) {
                    jCBTipo.setSelectedIndex(i);
                    break;
                }
            }
            activarCampos();
            jBGuardar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Paciente no encontrado");
        }
    }//GEN-LAST:event_jBBuscarActionPerformed

    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        try {
            String nombre = jTFNombre.getText();
            Integer calorias_por_100g = Integer.valueOf(jTFCalorias_por_100g.getText());
            String tipo = String.valueOf(jCBTipo.getSelectedItem());
            String detalle = jTADetalle.getText();
            if (comidaActual != null) {
                comidaActual.setNombre(nombre);
                comidaActual.setCalorias_por_100g(calorias_por_100g);
                comidaActual.setTipo(tipo);
                comidaActual.setDetalle(detalle);
                cd.modificarComida(comidaActual);
            } else {
                comidaActual = new Comida();
                comidaActual.setNombre(nombre);
                comidaActual.setCalorias_por_100g(calorias_por_100g);
                comidaActual.setTipo(tipo);
                comidaActual.setDetalle(detalle);
                cd.guardarComida(comidaActual);
            }
            jLID.setText("ID: " + comidaActual.getId_comida());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error al ingresar los tipos de datos, revise");
        }
    }//GEN-LAST:event_jBGuardarActionPerformed


